def compute_uncertainty(predictions_array: np.ndarray) -> dict[str, np.ndarray]:
    """
    Compute mean, std, and 95% confidence intervals from multi-model predictions.

    Parameters
    ----------
    predictions_array : np.ndarray, shape (n_models, n_samples)
        Each row is one model's prediction vector.

    Returns
    -------
    dict with keys:
        'mean'  – per-sample mean across models
        'std'   – per-sample standard deviation across models
        'lower' – lower bound of 95% CI  (mean - 1.96 * std)
        'upper' – upper bound of 95% CI  (mean + 1.96 * std)
        'ci_width' – width of the 95% CI (upper - lower)
    """
    # Use nanmean / nanstd so models with partial NaNs don't poison everything
    mean  = np.nanmean(predictions_array, axis=0)
    std   = np.nanstd(predictions_array,  axis=0)
    lower = mean - 1.96 * std
    upper = mean + 1.96 * std
    return {
        "mean":     mean,
        "std":      std,
        "lower":    lower,
        "upper":    upper,
        "ci_width": upper - lower,
    }


def compute_residual_uncertainty(
    y_true: np.ndarray,
    y_pred: np.ndarray,
    window: int = 50,
) -> np.ndarray:
    """
    Estimate depth-varying uncertainty from rolling residual standard deviation.

    Parameters
    ----------
    y_true  : ground-truth array  (may contain NaN)
    y_pred  : predicted array     (may contain NaN)
    window  : rolling window size (number of depth samples)

    Returns
    -------
    np.ndarray of the same length as y_true — rolling std of residuals.
    NaN where residuals are not computable.
    """
    residual = np.asarray(y_true, dtype=float) - np.asarray(y_pred, dtype=float)
    s = pd.Series(residual)
    rolling_std = s.rolling(window=window, center=True, min_periods=5).std()
    return rolling_std.values


def _plot_uncertainty_depth(
    df: pd.DataFrame,
    unc_dict: dict[str, np.ndarray],
    true_col: str | None,
    target_col: str,
) -> go.Figure:
    """
    Depth track with shaded 95% confidence interval and ensemble mean.
    """
    depth_col = _depth_col(df)
    depth = df[depth_col].values if depth_col else np.arange(len(df))

    mean  = unc_dict["mean"]
    lower = unc_dict["lower"]
    upper = unc_dict["upper"]

    fig = go.Figure()

    # 95% CI shaded band
    fig.add_trace(go.Scatter(
        x=np.concatenate([upper, lower[::-1]]),
        y=np.concatenate([depth, depth[::-1]]),
        fill="toself",
        fillcolor="rgba(25,118,210,0.18)",
        line=dict(color="rgba(0,0,0,0)"),
        name="95% CI",
        hoverinfo="skip",
    ))

    # Ensemble mean
    fig.add_trace(go.Scatter(
        x=mean, y=depth, mode="lines",
        line=dict(color="#1976D2", width=2),
        name="Ensemble Mean",
    ))

    # Ground truth if available
    if true_col and true_col in df.columns:
        fig.add_trace(go.Scatter(
            x=df[true_col].values, y=depth, mode="lines",
            line=dict(color="#E53935", dash="dot", width=1.5),
            name="Ground Truth",
        ))

    fig.update_yaxes(autorange="reversed", title="Depth")
    fig.update_xaxes(title=f"{target_col} (µs/ft)")
    fig.update_layout(
        title=f"{target_col} — Ensemble Mean ± 95% Confidence Interval",
        height=520,
        hovermode="y unified",
        margin=dict(l=60, r=20, t=55, b=40),
        legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="right", x=1),
    )
    return fig



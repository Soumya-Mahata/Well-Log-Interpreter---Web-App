def compute_metrics(y_true: np.ndarray, y_pred: np.ndarray) -> dict[str, float | None]:
    """
    Compute RMSE, MAE, and R² safely, ignoring NaNs.

    Parameters
    ----------
    y_true : ground-truth array (may contain NaN)
    y_pred : predicted array   (may contain NaN)

    Returns
    -------
    dict with keys 'RMSE', 'MAE', 'R²'  — float or None if insufficient data
    """
    y_true = np.asarray(y_true, dtype=float)
    y_pred = np.asarray(y_pred, dtype=float)

    valid = ~(np.isnan(y_true) | np.isnan(y_pred))
    n_valid = int(valid.sum())

    if n_valid < 2:
        return {"RMSE": None, "MAE": None, "R²": None}

    yt = y_true[valid]
    yp = y_pred[valid]

    residuals = yt - yp
    rmse = float(np.sqrt(np.mean(residuals ** 2)))
    mae  = float(np.mean(np.abs(residuals)))

    ss_res = float(np.sum(residuals ** 2))
    ss_tot = float(np.sum((yt - np.mean(yt)) ** 2))
    r2     = (1.0 - ss_res / ss_tot) if ss_tot > 0 else None

    return {"RMSE": rmse, "MAE": mae, "R²": r2}


def get_available_models(df: pd.DataFrame, target_col: str) -> dict[str, str]:
    """
    Dynamically detect all prediction columns relevant to `target_col`.

    Strategy
    --------
    1. ML/DL per-model columns: pattern  '<target>_<model_key>_pred'
       e.g.  DTC_xgboost_pred, DTC_cnn_bilstm_pred
    2. Legacy ML/DL column: exact pattern '<target>_pred'  (backward compat)
    3. Conventional: columns starting with velocity-domain tag
       ('Vp' for DTC, 'Vs' for DTS) that contain 'pred'
    4. Fallback: any column containing the target name and 'pred'

    Returns
    -------
    dict { display_label → df_column_name }
    """
    _ML_KEYS = [
        "random_forest", "xgboost", "cnn_bilstm",
    ]
    _ML_LABELS = {
        "random_forest": "Random Forest",
        "xgboost":       "XGBoost",
        "cnn_bilstm":    "CNN-BiLSTM",
    }

    models: dict[str, str] = {}
    seen_cols: set[str] = set()

    # 1. Per-model ML/DL columns ──────────────────────────────────────────────
    for key in _ML_KEYS:
        col = f"{target_col}_{key}_pred"
        if col in df.columns and col not in seen_cols:
            label = _ML_LABELS.get(key, key)
            models[f"[ML/DL] {label}"] = col
            seen_cols.add(col)

    # 2. Legacy single ML/DL column ───────────────────────────────────────────
    legacy_col = f"{target_col}{_UNC_SUFFIX}"
    if legacy_col in df.columns and legacy_col not in seen_cols:
        models[f"[ML/DL] {legacy_col}"] = legacy_col
        seen_cols.add(legacy_col)

    # 3. Conventional — velocity-domain prefix ────────────────────────────────
    vel_tag = "Vp" if target_col == "DTC" else "Vs"
    for col in df.columns:
        if col in seen_cols:
            continue
        if col.startswith(vel_tag) and "pred" in col.lower():
            models[f"[Conv] {col}"] = col
            seen_cols.add(col)

    # 4. Fallback — any col containing target name + pred ─────────────────────
    tgt_lower = target_col.lower()
    for col in df.columns:
        if col in seen_cols:
            continue
        cl = col.lower()
        if tgt_lower in cl and "pred" in cl:
            models[f"[Conv] {col}"] = col
            seen_cols.add(col)

    return models


def _depth_col(df: pd.DataFrame) -> str | None:
    """Return best-guess depth column name, or None."""
    return find_col(df, ["DEPTH", "MD", "DEPT", "TVD", "DEPTH_MD"])



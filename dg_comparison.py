def _plot_uncertainty_crossplot(
    y_true: np.ndarray,
    y_mean: np.ndarray,
    y_std: np.ndarray,
    target_col: str,
) -> go.Figure:
    """
    Actual vs Ensemble-Mean scatter, coloured by prediction uncertainty (std).
    """
    valid = ~(np.isnan(y_true) | np.isnan(y_mean) | np.isnan(y_std))
    yt = y_true[valid]
    yp = y_mean[valid]
    ys = y_std[valid]

    if len(yt) < 2:
        fig = go.Figure()
        fig.update_layout(title="Not enough valid data for crossplot")
        return fig

    mn = min(yt.min(), yp.min())
    mx = max(yt.max(), yp.max())

    fig = go.Figure()
    # 1:1 line
    fig.add_trace(go.Scatter(
        x=[mn, mx], y=[mn, mx], mode="lines",
        line=dict(color="gray", dash="dash", width=1),
        name="1:1", showlegend=True,
    ))
    # Scatter coloured by uncertainty
    fig.add_trace(go.Scatter(
        x=yt, y=yp, mode="markers",
        marker=dict(
            color=ys,
            colorscale="YlOrRd",
            size=5,
            opacity=0.75,
            colorbar=dict(title="Std Dev", thickness=14),
            showscale=True,
        ),
        name="Samples",
        hovertemplate=(
            f"Actual: %{{x:.2f}}<br>Predicted: %{{y:.2f}}<br>Std: %{{marker.color:.2f}}<extra></extra>"
        ),
    ))
    fig.update_layout(
        title=f"{target_col} — Actual vs Ensemble Mean (colour = uncertainty)",
        xaxis_title=f"Actual {target_col}",
        yaxis_title=f"Ensemble Mean {target_col}",
        height=450,
        margin=dict(l=60, r=20, t=55, b=40),
    )
    return fig


def _plot_uncertainty_histogram(
    std_array: np.ndarray,
    target_col: str,
) -> go.Figure:
    """
    Distribution of per-depth uncertainty (std) values.
    """
    valid = std_array[~np.isnan(std_array)]
    fig = go.Figure()
    fig.add_trace(go.Histogram(
        x=valid,
        nbinsx=40,
        marker_color="#1976D2",
        opacity=0.8,
        name="Std Dev",
    ))
    # Annotate median and p90
    if len(valid) > 0:
        med = float(np.median(valid))
        p90 = float(np.percentile(valid, 90))
        fig.add_vline(x=med, line_dash="dash", line_color="#E53935",
                      annotation_text=f"Median: {med:.3f}",
                      annotation_position="top right")
        fig.add_vline(x=p90, line_dash="dot",  line_color="#F57F17",
                      annotation_text=f"P90: {p90:.3f}",
                      annotation_position="top left")
    fig.update_layout(
        title=f"{target_col} — Distribution of Prediction Uncertainty (Std Dev)",
        xaxis_title=f"Std Dev of {target_col} Predictions",
        yaxis_title="Count",
        height=380,
        margin=dict(l=60, r=20, t=55, b=40),
    )
    return fig


def _render_uncertainty_section(
    df: pd.DataFrame,
    model_rows: list[dict],
    target_col: str,
    true_col: str | None,
    section_label: str = "H",
) -> None:
    """
    Full Streamlit UI block for uncertainty quantification.
    Renders only when the parent checkbox is enabled.
    """
    st.markdown(f"### {section_label} · Uncertainty Quantification")
    st.markdown(
        """
        **Multi-model ensemble uncertainty** uses the spread across all selected models
        as a proxy for prediction uncertainty.  
        - **Std Dev** captures *epistemic* uncertainty (model disagreement).  
        - **95% CI** = Mean ± 1.96 × Std Dev.  
        - High-uncertainty zones may indicate poor data quality, complex lithology, or model instability.
        """
    )

    # Gather prediction arrays for all valid model columns
    pred_cols = [r["Column"] for r in model_rows if r["Column"] in df.columns]
    n_models  = len(pred_cols)

    if n_models < 2:
        st.warning(
            "⚠️ Uncertainty analysis requires **at least 2 models**.  "
            "Select more models in Section B."
        )
        return

    # Build (n_models, n_samples) array
    pred_matrix = np.vstack([df[c].values.astype(float) for c in pred_cols])
    unc = compute_uncertainty(pred_matrix)

    # ── Summary metrics ──────────────────────────────────────────────────────
    st.markdown("#### Summary Metrics")
    m1, m2, m3, m4 = st.columns(4)
    m1.metric("Models",           f"{n_models}")
    m2.metric("Mean Std Dev",     f"{float(np.nanmean(unc['std'])):.4f}")
    m3.metric("Mean CI Width",    f"{float(np.nanmean(unc['ci_width'])):.4f}")
    m4.metric("Max Std Dev",      f"{float(np.nanmax(unc['std'])):.4f}")

    # ── Per-model average uncertainty ────────────────────────────────────────
    st.markdown("#### Per-Model Average Uncertainty Contribution")
    per_model_data = []
    for col, row in zip(pred_cols, model_rows):
        vals = df[col].values.astype(float)
        abs_dev = np.abs(vals - unc["mean"])
        per_model_data.append({
            "Model":           row["Model"],
            "Avg |Deviation|": float(np.nanmean(abs_dev)),
            "Max |Deviation|": float(np.nanmax(abs_dev)),
        })
    per_model_df = pd.DataFrame(per_model_data).sort_values(
        "Avg |Deviation|", ascending=False
    ).reset_index(drop=True)
    st.dataframe(
        per_model_df.style.format({
            "Avg |Deviation|": "{:.5f}",
            "Max |Deviation|": "{:.5f}",
        }),
        use_container_width=True,
        hide_index=True,
    )

    st.divider()

    # ── Toggle: confidence interval depth plot ───────────────────────────────
    show_ci = st.checkbox(
        "Show 95% Confidence Interval (depth track)",
        value=True,
        key=f"dg_unc_ci_toggle_{target_col}",
    )
    if show_ci:
        st.plotly_chart(
            _plot_uncertainty_depth(df, unc, true_col, target_col),
            use_container_width=True,
            key=f"dg_unc_depth_{target_col}",
        )

    # ── Toggle: uncertainty magnitude depth plot ─────────────────────────────
    show_std = st.checkbox(
        "Show per-depth Std Dev (uncertainty magnitude)",
        value=True,
        key=f"dg_unc_std_toggle_{target_col}",
    )
    if show_std:
        st.plotly_chart(
            _plot_uncertainty_std_depth(df, unc["std"], target_col),
            use_container_width=True,
            key=f"dg_unc_std_{target_col}",
        )

    # ── Crossplot coloured by uncertainty (needs ground truth) ───────────────
    if true_col and true_col in df.columns:
        show_cross = st.checkbox(
            "Show Actual vs Predicted crossplot (coloured by uncertainty)",
            value=True,
            key=f"dg_unc_cross_toggle_{target_col}",
        )
        if show_cross:
            y_true = df[true_col].values.astype(float)
            st.plotly_chart(
                _plot_uncertainty_crossplot(y_true, unc["mean"], unc["std"], target_col),
                use_container_width=True,
                key=f"dg_unc_cross_{target_col}",
            )
    else:
        st.caption("ℹ️ Crossplot requires a ground-truth column (Section A).")

    # ── Uncertainty histogram ────────────────────────────────────────────────
    show_hist = st.checkbox(
        "Show uncertainty distribution histogram",
        value=True,
        key=f"dg_unc_hist_toggle_{target_col}",
    )
    if show_hist:
        st.plotly_chart(
            _plot_uncertainty_histogram(unc["std"], target_col),
            use_container_width=True,
            key=f"dg_unc_hist_{target_col}",
        )

    # ── Residual-based uncertainty (optional, needs GT) ──────────────────────
    if true_col and true_col in df.columns:
        st.divider()
        with st.expander("🔬 Residual-Based Uncertainty (optional, single best model)", expanded=False):
            st.markdown(
                "Rolling standard deviation of residuals from the best-performing model.  \n"
                "High values highlight depth intervals where even the best model struggles."
            )
            best_row = model_rows[0]  # already sorted by RMSE
            y_true = df[true_col].values.astype(float)
            y_pred = df[best_row["Column"]].values.astype(float)
            window = st.slider(
                "Rolling window (depth samples)",
                min_value=10, max_value=200, value=50, step=10,
                key=f"dg_unc_window_{target_col}",
            )
            resid_std = compute_residual_uncertainty(y_true, y_pred, window=window)
            depth_col = _depth_col(df)
            depth_vals = df[depth_col].values if depth_col else np.arange(len(df))

            fig_res = go.Figure()
            fig_res.add_trace(go.Scatter(
                x=resid_std, y=depth_vals, mode="lines",
                line=dict(color="#2E7D32", width=1.8),
                fill="tozerox",
                fillcolor="rgba(46,125,50,0.15)",
                name="Residual Std",
            ))
            fig_res.update_yaxes(autorange="reversed", title="Depth")
            fig_res.update_xaxes(title=f"Residual Std of {target_col}")
            fig_res.update_layout(
                title=f"{target_col} — Residual-Based Uncertainty (Best Model: {best_row['Model']})",
                height=480, hovermode="y unified",
                margin=dict(l=60, r=20, t=55, b=40),
            )
            st.plotly_chart(fig_res, use_container_width=True,
                            key=f"dg_unc_resid_{target_col}")

    # ── Save ensemble mean to session ────────────────────────────────────────
    st.divider()
    st.markdown("#### 💾 Save Ensemble Mean as Output Column")
    ens_col = f"{target_col}_ensemble_mean"
    if st.button(
        f"Save ensemble mean → `{ens_col}`",
        key=f"dg_unc_save_{target_col}",
    ):
        st.session_state.df[ens_col] = unc["mean"]
        st.session_state.df[f"{target_col}_ensemble_std"]   = unc["std"]
        st.session_state.df[f"{target_col}_ensemble_lower"] = unc["lower"]
        st.session_state.df[f"{target_col}_ensemble_upper"] = unc["upper"]
        n_saved = int(pd.Series(unc["mean"]).notna().sum())
        st.success(
            f"✅ Saved `{ens_col}` and associated uncertainty columns "
            f"({n_saved:,} values). Columns added:\n"
            f"- `{target_col}_ensemble_mean`\n"
            f"- `{target_col}_ensemble_std`\n"
            f"- `{target_col}_ensemble_lower`\n"
            f"- `{target_col}_ensemble_upper`"
        )



def plot_comparison(
    metrics_df: pd.DataFrame,
    metric: str,
    target_col: str,
) -> None:
    """
    Horizontal bar chart for a single metric.
    Green = best, red = worst, blue = middle.
    """
    sub = metrics_df.dropna(subset=[metric]).copy()
    if sub.empty:
        st.info(f"No valid {metric} values to plot.")
        return

    ascending = metric in ("RMSE", "MAE")   # lower is better
    sub = sub.sort_values(metric, ascending=ascending).reset_index(drop=True)

    n = len(sub)
    bar_colors = []
    for i in range(n):
        if i == 0:
            bar_colors.append(COLORS[2])   # green = best
        elif i == n - 1:
            bar_colors.append(COLORS[1])   # red = worst
        else:
            bar_colors.append(COLORS[0])   # blue = middle

    fig = go.Figure(
        go.Bar(
            x=sub[metric],
            y=sub["Model"],
            orientation="h",
            marker_color=bar_colors,
            text=sub[metric].apply(lambda v: f"{v:.4f}"),
            textposition="outside",
        )
    )
    direction = "lower = better" if ascending else "higher = better"
    fig.update_layout(
        title=f"{metric} by model ({direction})",
        height=max(280, 50 * n + 80),
        xaxis_title=metric,
        yaxis_title="",
        yaxis=dict(autorange="reversed"),
        margin=dict(l=20, r=70, t=50, b=40),
    )
    st.plotly_chart(
        fig,
        use_container_width=True,
        key=f"dg_cmp_bar_{target_col}_{metric}",
    )


def _plot_depth_overlay(
    df: pd.DataFrame,
    true_col: str | None,
    model_rows: list[dict],
    target_col: str,
) -> None:
    """Depth-track overlay for all selected models + ground truth."""
    curves, clabels, colors = [], [], []

    if true_col and true_col in df.columns:
        curves.append(true_col)
        clabels.append("Ground Truth")
        colors.append("#212121")

    for i, row in enumerate(model_rows):
        col = row["Column"]
        if col in df.columns:
            curves.append(col)
            clabels.append(row["Model"])
            colors.append(COLORS[i % len(COLORS)])

    if not curves:
        st.info("No data available for depth-track plot.")
        return

    fig = plotly_depth_track(
        df, curves, clabels, colors,
        f"{target_col} — Predictions vs Depth",
        target_col,
    )
    st.plotly_chart(fig, use_container_width=True, key=f"dg_cmp_depth_{target_col}")



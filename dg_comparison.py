def render(df: pd.DataFrame) -> None:  # noqa: C901  (complexity OK for UI module)
    section_header(
        "📊",
        "Model Comparison & Final Selection",
        "Compare conventional and ML/DL predictions for DTC / DTS sonic logs",
    )

    # Guard ───────────────────────────────────────────────────────────────────
    if "df" not in st.session_state or st.session_state.df is None:
        st.warning("No dataset loaded. Upload data first.")
        return

    df = st.session_state.df   # always reflect live session

    # ═══════════════════════════════════════════════════════════════════════ #
    # SECTION A — Target & Ground-Truth
    # ═══════════════════════════════════════════════════════════════════════ #
    st.markdown("### A · Select Target & Ground Truth")

    col_a1, col_a2 = st.columns(2)

    target_display = col_a1.selectbox(
        "Select Sonic Target for Comparison",
        options=["DTC (P-wave Sonic)", "DTS (S-wave Sonic)"],
        key="dg_cmp_target",
        help="DTC = compressional slowness. DTS = shear slowness.",
    )
    target_col = "DTC" if "DTC" in target_display else "DTS"

    num  = numeric_cols(df)
    opts = ["None"] + num
    auto_gt = find_col(df, _GT_CANDIDATES.get(target_col, [target_col]))
    gt_idx  = opts.index(auto_gt) if (auto_gt and auto_gt in opts) else 0

    true_col_sel = col_a2.selectbox(
        f"Ground-Truth Column for {target_col}",
        options=opts,
        index=gt_idx,
        key="dg_cmp_gt_col",
        help="Optional — required to compute RMSE / MAE / R².",
    )
    true_col: str | None = None if true_col_sel == "None" else true_col_sel

    if true_col is None:
        st.info(
            "ℹ️ No ground-truth column selected — metrics cannot be computed. "
            "Visual comparisons and final model selection are still available."
        )

    st.divider()

    # ═══════════════════════════════════════════════════════════════════════ #
    # SECTION B — Detect Available Prediction Columns
    # ═══════════════════════════════════════════════════════════════════════ #
    available_models: dict[str, str] = get_available_models(df, target_col)

    if not available_models:
        st.warning(
            f"No prediction columns found for **{target_col}**.\n\n"
            "Run the **Conventional** or **Unconventional** workflows first, "
            "then return here."
        )
        return

    # ═══════════════════════════════════════════════════════════════════════ #
    # SECTION C — Model Multi-Select
    # ═══════════════════════════════════════════════════════════════════════ #
    st.markdown("### B · Select Models to Compare")

    all_labels = list(available_models.keys())

    selected_labels: list[str] = st.multiselect(
        "Select Models to Compare",
        options=all_labels,
        default=all_labels,
        key="dg_cmp_model_select",
        help="All detected prediction columns are listed. Deselect any you wish to exclude.",
    )

    if not selected_labels:
        st.info("Select at least one model to continue.")
        return

    # Build unified row records
    model_rows: list[dict] = []
    for lbl in selected_labels:
        col = available_models[lbl]
        if col not in df.columns:
            st.caption(f"⚠️ Column `{col}` missing from dataset — skipped.")
            continue

        if true_col and true_col in df.columns:
            m = compute_metrics(
                df[true_col].values.astype(float),
                df[col].values.astype(float),
            )
        else:
            m = {"RMSE": None, "MAE": None, "R²": None}

        model_rows.append({
            "Model":  lbl.split("] ", 1)[-1],   # short name without tag
            "Column": col,
            "Type":   "ML/DL" if lbl.startswith("[ML") else "Conventional",
            "RMSE":   m["RMSE"],
            "MAE":    m["MAE"],
            "R²":     m["R²"],
        })

    if not model_rows:
        st.warning("No valid prediction data found for the selected models.")
        return

    metrics_df = pd.DataFrame(model_rows)
    has_metrics = metrics_df["RMSE"].notna().any()

    # Sort by RMSE ascending when metrics are available
    if has_metrics:
        metrics_df = metrics_df.sort_values("RMSE", ascending=True).reset_index(drop=True)
        model_rows = metrics_df.to_dict("records")

    st.divider()

    # ═══════════════════════════════════════════════════════════════════════ #
    # SECTION D — Metrics Table
    # ═══════════════════════════════════════════════════════════════════════ #
    st.markdown("### C · Performance Metrics")

    display_df = metrics_df[["Model", "Type", "RMSE", "MAE", "R²"]].copy()
    fmt = {c: "{:.5f}" for c in ["RMSE", "MAE", "R²"] if c in display_df.columns}

    st.dataframe(
        display_df.style.format(fmt, na_rep="—"),
        use_container_width=True,
        hide_index=True,
    )

    if not has_metrics:
        st.caption(
            "Assign a ground-truth column (Section A) to compute RMSE / MAE / R²."
        )

    st.divider()

    # ═══════════════════════════════════════════════════════════════════════ #
    # SECTION E — Bar Charts
    # ═══════════════════════════════════════════════════════════════════════ #
    st.markdown("### D · Metric Comparison Charts")

    if has_metrics:
        tab_rmse, tab_mae, tab_r2 = st.tabs(["RMSE ↓", "MAE ↓", "R² ↑"])
        with tab_rmse:
            plot_comparison(metrics_df, "RMSE", target_col)
        with tab_mae:
            plot_comparison(metrics_df, "MAE",  target_col)
        with tab_r2:
            plot_comparison(metrics_df, "R²",   target_col)
    else:
        st.info("Metric charts require a ground-truth column.")

    st.divider()

    # ═══════════════════════════════════════════════════════════════════════ #
    # SECTION F — Depth-Track Visualisation
    # ═══════════════════════════════════════════════════════════════════════ #
    st.markdown("### E · Depth-Track Visualisation")

    show_depth = st.checkbox(
        "Show depth-track overlay",
        value=True,
        key="dg_cmp_depth_toggle",
    )

    if show_depth:
        _plot_depth_overlay(df, true_col, model_rows, target_col)

        # Focused scatter + residual
        if true_col and true_col in df.columns:
            st.markdown("**Scatter & residual — focused on one model:**")
            focus_short = st.selectbox(
                "Model for detail view",
                options=[r["Model"] for r in model_rows],
                key="dg_cmp_focus_model",
            )
            focus_row = next((r for r in model_rows if r["Model"] == focus_short), None)

            if focus_row and focus_row["Column"] in df.columns:
                yt = df[true_col].values.astype(float)
                yp = df[focus_row["Column"]].values.astype(float)

                sc1, sc2 = st.columns(2)
                with sc1:
                    st.plotly_chart(
                        plotly_scatter(yt, yp, target_col, focus_short),
                        use_container_width=True,
                        key=f"dg_cmp_scatter_{target_col}",
                    )
                with sc2:
                    st.plotly_chart(
                        plotly_residual_hist(yt, yp, target_col),
                        use_container_width=True,
                        key=f"dg_cmp_resid_{target_col}",
                    )

    st.divider()

    # ═══════════════════════════════════════════════════════════════════════ #
    # SECTION G — Best Model Banner
    # ═══════════════════════════════════════════════════════════════════════ #
    st.markdown("### F · Best Model")

    if has_metrics:
        best = metrics_df.iloc[0]    # sorted by RMSE ascending
        best_name = best["Model"]
        bg1, bg2, bg3 = st.columns(3)
        bg1.success(f"🏆 **Best Model:** {best_name}")
        if best["RMSE"] is not None:
            bg2.metric("Best RMSE", f"{best['RMSE']:.4f}")
        if best["R²"] is not None:
            bg3.metric("Best R²", f"{best['R²']:.4f}")
    else:
        best_name = None
        st.info(
            "Automatic best-model ranking requires a ground-truth column. "
            "You can still manually select the final model below."
        )

    st.divider()

    # ═══════════════════════════════════════════════════════════════════════ #
    # SECTION H — Final Model Selection & Save
    # ═══════════════════════════════════════════════════════════════════════ #
    st.markdown("### G · Final Model Selection")

    final_out_col = f"{target_col}_final"
    st.caption(
        f"The selected model's predictions will be saved as **`{final_out_col}`** "
        "in your dataset and can be used downstream."
    )

    short_names = [r["Model"] for r in model_rows]
    default_idx = 0
    if best_name and best_name in short_names:
        default_idx = short_names.index(best_name)

    final_label = st.selectbox(
        "Select Model to Use as Final Output",
        options=short_names,
        index=default_idx,
        key="dg_cmp_final_select",
        help="Pre-selected to the best-performing model (lowest RMSE) when metrics are available.",
    )

    final_row = next((r for r in model_rows if r["Model"] == final_label), None)
    if final_row is None:
        st.error("Could not resolve the selected model.")
        return

    final_src = final_row["Column"]
    col_h1, col_h2 = st.columns([3, 1])
    col_h1.markdown(f"**Source:** `{final_src}` → **Output:** `{final_out_col}`")

    if col_h2.button("💾 Save as Final Output", type="primary", key="dg_cmp_save_btn"):
        if final_src not in df.columns:
            st.error(f"Source column `{final_src}` not found in dataset.")
        else:
            st.session_state.df[final_out_col] = st.session_state.df[final_src].copy()
            n_saved = int(st.session_state.df[final_out_col].notna().sum())
            st.success(
                f"✅ **{final_label}** saved to `{final_out_col}` — "
                f"{n_saved:,} values written."
            )
            vals = st.session_state.df[final_out_col].dropna()
            if len(vals):
                s1, s2, s3, s4 = st.columns(4)
                s1.metric("Count",  f"{len(vals):,}")
                s2.metric("Mean",   f"{vals.mean():.3f}")
                s3.metric("Min",    f"{vals.min():.3f}")
                s4.metric("Max",    f"{vals.max():.3f}")

    # Status if final column already exists
    if final_out_col in st.session_state.df.columns:
        n_exist = int(st.session_state.df[final_out_col].notna().sum())
        st.caption(
            f"ℹ️ `{final_out_col}` already exists with {n_exist:,} non-null values. "
            "Saving will overwrite it."
        )

    st.divider()

    # ═══════════════════════════════════════════════════════════════════════ #
    # SECTION I — Uncertainty Quantification
    # ═══════════════════════════════════════════════════════════════════════ #

    enable_unc = st.checkbox(
        "🔍 Enable Uncertainty Analysis",
        value=False,
        key=f"dg_cmp_unc_enable_{target_col}",
        help=(
            "Quantify prediction uncertainty using multi-model ensemble spread. "
            "Requires ≥2 models selected above."
        ),
    )

    if enable_unc:
        _render_uncertainty_section(
            df=st.session_state.df,
            model_rows=model_rows,
            target_col=target_col,
            true_col=true_col,
            section_label="I",
        )
    else:
        st.info(
            "☝️ Enable **Uncertainty Analysis** above to quantify and visualise "
            "prediction confidence intervals and model disagreement."
        )


